def initialize_skills(db, SkillCategory, Skill):
    """
    تهيئة قاعدة بيانات المهارات الـ 383
    Initialize the 383 skills database
    """
    print("بدء تهيئة قاعدة بيانات المهارات...")
    
    total_skills = 0
    order_index = 0
    
    for category_name, category_data in SKILL_CATEGORIES.items():
        # إنشاء الفئة
        category = SkillCategory.query.filter_by(name=category_name).first()
        if not category:
            category = SkillCategory(
                name=category_name,
                description=category_data["description"],
                order_index=order_index,
                is_active=True,
                created_at=datetime.utcnow()
            )
            db.session.add(category)
            db.session.flush()
            print(f"تم إنشاء الفئة: {category_name}")
        
        # إنشاء المهارات
        for skill_order, skill_name in enumerate(category_data["skills"], 1):
            skill = Skill.query.filter_by(
                category_id=category.id,
                name=skill_name
            ).first()
            
            if not skill:
                skill = Skill(
                    category_id=category.id,
                    name=skill_name,
                    skill_number=f"{order_index + 1}.{skill_order}",
                    description=f"مهارة: {skill_name}",
                    level="أساسي",
                    order_index=skill_order,
                    is_active=True,
                    created_at=datetime.utcnow()
                )
                db.session.add(skill)
                total_skills += 1
        
        order_index += 1
        print(f"تم إضافة {category_data['count']} مهارة في فئة {category_name}")
    
    db.session.commit()
    print(f"\nتم تهيئة {total_skills} مهارة بنجاح!")
    print(f"إجمالي الفئات: {len(SKILL_CATEGORIES)}")
    
    return total_skills


